    private void addDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addDataActionPerformed
        if(TextFieldofData.getText().equals(""))
            return;
        dataModel.addData(TextFieldofData.getText());
        TextFieldofData.setText("");
        tableofData.updateUI();
    }//GEN-LAST:event_addDataActionPerformed

    private void buttonOfCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonOfCalculateActionPerformed

        int n = dataModel.getRowCount();
        float uncertaintyB = Float.parseFloat(TextFieldOfUncertaintyB.getText());
        float[] list = new float[n];
        for(int i=0;i<n;i++){
            list[i] = Float.parseFloat(dataModel.getValueAt(i, 1).toString());
        }
        Calculate calculate = new Calculate(list,uncertaintyB);
        TextAreaofOutput.append("UncertaintyA=" + calculate.getUncertaintyA() + "\n");
        TextAreaofOutput.append("UncertaintyB=" + uncertaintyB + "\n");
        TextAreaofOutput.append("Uncertainty=" + calculate.getUncertainty());
        
    }//GEN-LAST:event_buttonOfCalculateActionPerformed

